<H1>閏年判斷</H1>
公元年分除以4可整除，為閏年。<br>
公元年分除以4可整除但除以100不可整除，為閏年。<br>
公元年分除以100不可整除但除以400可整除，為閏年。<br>
<?php

$years=[];
for ($i=2023; $i < 2523 ; $i++) {

    if (($i %4 == 0) && ($i %100 != 0 ) || ($i %100 != 0 && $i%400 == 0 ) ) {
       $years[]=$i;
    }
}
foreach($years as $y){
    echo $y."<br>";
}

$t=2351;

if(in_array($t,$years)){
    echo $t."是閏年";
}else{
    echo $t."是平年";
}